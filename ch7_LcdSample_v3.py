def ActivateLCD():
    wp.digitalWrite(pin_CS_lcd, 0)       # Actived LCD using CS
    time.sleep(0.000005)

def DeactivateLCD():
    wp.digitalWrite(pin_CS_lcd, 1)       # Deactived LCD using CS
    time.sleep(0.000005)

PIN_OUT     =   {  
                'SCLK'  :   14,
                'DIN'   :   12,
                'DC'    :   8, 
                'CS'    :   15, #We will not connect this pin! --> we use w13
                'RST'   :   9,
                'LED'   :   7, #backlight   
}

#IN THIS CODE WE USE W13 (PIN 22) AS CHIP SELECT
pin_CS_lcd = 1
wp.wiringPiSetup() 
wp.wiringPiSPISetupMode(0, 400000, 0)  #(channel, speed, mode)
wp.pinMode(pin_CS_lcd , 1)            # Set pin to mode 1 ( OUTPUT )
ActivateLCD()
lcd_1 = LCD(PIN_OUT)

threshold = 85

lcd_1.clear()
lcd_1.set_backlight(1)
try:
    gif = Image.open('pingu.gif')
    while True:
        for f in range(0,gif.n_frames):
            gif.seek(f)
            ActivateLCD()
            lcd_1.clear()
            lcd_1.go_to_xy(13, 0)
            #lcd_1.put_string('LCD_Sample:' + str(i))
            #lcd_1.draw_logo()
            lcd_1.draw_PIL_image(gif.resize((lcd_1.width, lcd_1.height)))
            lcd_1.refresh()
            DeactivateLCD()
            time.sleep(gif.info['duration']/1000.0)
except (Exception, KeyboardInterrupt):
    #print(traceback.format_exc())
    lcd_1.clear()
    lcd_1.refresh()
    lcd_1.set_backlight(0)
    DeactivateLCD()
    print("\nProgram terminated")


